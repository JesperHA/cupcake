package ModelLayer;

public class Bottom {
    int idbottom;
    String smag;
    int pris;

    public Bottom(int idbottom, String smag, int pris) {
        this.idbottom = idbottom;
        this.smag = smag;
        this.pris = pris;
    }

    public int getIdbottom() {
        return idbottom;
    }

    public String getSmag() {
        return smag;
    }

    public int getPris() {
        return pris;
    }
}
